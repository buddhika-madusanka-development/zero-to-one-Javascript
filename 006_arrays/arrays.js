// What is an array?
// An array is a special variable that holds multiple values in a single place!

// Basic Array functions
// push() => Adds an item to the end: fruits.push('Grapes');
// pop() => Removes the last item: fruits.pop();
// unshift() => Adds an item to the start: fruits.unshift('Mango');
// shift() => Removes the first item: fruits.shift();
// indexOf() => Returns the index of an item: fruits.indexOf('Banana');
// includes() => Returns true if an item exists: fruits.includes('Banana');
// splice() => Removes items from an array: fruits.splice(1, 2);
// slice() => Returns a portion of an array: fruits.slice(1, 3);

// Advanced Array functions
// sort() => Sorts the array: fruits.sort(); // Alphabetical
// forEach() => Loops through each item: fruits.forEach(fruit => console.log(fruit));
// map() => Creates a new array by modifying each item: numbers.map(num => num * 2);
// filter() => Returns items that match a condition: let shortWords = words.filter(w => w.length < 5);
// reduce() => Reduces an array to a single value: let total = numbers.reduce((acc, num) => acc + num, 0);
// find() => Returns the first item that matches a condition: let bigNum = numbers.find(n => n > 10);

